        <div class="solutions">
       	  <h3>Themes</h3>
            <p>
            	
 	<span style="font-size: 16px"> The Centre’s research work is focussed around the following five themes:
        
          <p>&nbsp;</p> 
          <h4>Geospatial Applications of Unmanned Aerial Vehicles (UAVs)</h4>
          <p>&nbsp;</p> 
          
          <p>This theme covers applications of UAVs in areas including volcanology, disaster management and freshwater ecology; comparisons of different UAV platforms and configurations; integration of UAVs with other applications, the use of UAVs to communicate with scientific and user communities; low cost UAV technologies.</p>
          
          <p>&nbsp;</p>
          
          <p>Impacts:  UAVs can be used to collect data about the environment and monitor real time situations that cannot be accessed directly for safety or cost reasons (e.g. disaster events).</p>
           <p>&nbsp;</p>
           
           <p>Staff: Raj Prasanna (theme lead), Ian Fuller, Mike Touhy, Matthew Irwin, Russell Death</p>

          <p>&nbsp;</p>
          
          <hr />
           <p>&nbsp;</p> 
          <h4>Geographic Semantics and Communication</h4>
          <p>&nbsp;</p>
          
          <p>This theme explores the meaning of geographic concepts and terminology, how meaning can be represented in information systems, how we might deal with the potential issues that arise in communication between user communities that interpret geographic terms in different ways (with applications in agriculture, ecology and disaster management), and design of geographic user interfaces to enhance user engagement.</p>
          
          <p>&nbsp;</p>
          <p>Impacts:  Stakeholder engagement is key to the success of many projects across the agricultural, environmental and disaster fields.  This engagement can be destroyed by misunderstandings or by use of terminology that has different meanings in different user communities. This theme aims to improve understanding of concepts in the agriculture, ecology and across diverse groups more effective.</p>
           <p>&nbsp;</p>
           
           <p>Staff: Kristin Stock (theme lead), Russell Death, Emma Hudson-Doyle, Ian Yule.</p>

